#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	index;

	index = 0;
	if (n == 0)
		return (0);
	while (index + 1 < n && s1[index] && s1[index] == s2[index])
		index++;
	return (s1[index] - s2[index]);
}
